using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculatrice
{
    public class Calculatrice
    {
        public Calculatrice() { }

        public double Addition(double nbOne, double nbTwo)
        {
            return nbOne + nbTwo;
        }

        public double Soustraction(double nbOne, double nbTwo)
        {
            return nbOne - nbTwo;
        }
    }
}
